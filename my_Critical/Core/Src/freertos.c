void TxTask(void *parames){
	uint8_t TxData[8];
	for(;;){
		taskENTER_CRITICAL();
		HAL_UART_Receive_DMA(&huart2,TxData,sizeof(TxData));
		taskEXIT_CRITICAL();
		xQueueSend(xQueue,TxData,portMAX_DELAY);
		vTaskDelay(50);
	}
}
void RxTask(void *parames){
	uint8_t RxData[8];
	for(;;){
		xQueueReiceive(xQueue,RxData,portMAX_DELAY);
		if(RxData[0] == 0x01){
			uxStatus = taskENTER_CRITICAL();
			HAL_UART_Receive_DMA(&huart2,TxData,sizeof(TxData));
			taskEXIT_CRITICAL(uxStatus);
		}
		vTaskDelay(50);
	}
}
